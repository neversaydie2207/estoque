package com.fullstack.sic.pojo;

public class Endereco
{
	private String uf;
	private String estado;
	private String cidade;
	private String bairro;
	private String logradouro;
	private String logradouro_curto;

	public String getUf()
	{
		return uf;
	}

	public void setUf(String uf)
	{
		this.uf = uf;
	}

	public String getEstado()
	{
		return estado;
	}

	public void setEstado(String estado)
	{
		this.estado = estado;
	}

	public String getCidade()
	{
		return cidade;
	}

	public void setCidade(String cidade)
	{
		this.cidade = cidade;
	}

	public String getBairro()
	{
		return bairro;
	}

	public void setBairro(String bairro)
	{
		this.bairro = bairro;
	}

	public String getLogradouro()
	{
		return logradouro;
	}

	public void setLogradouro(String logradouro)
	{
		this.logradouro = logradouro;
	}

	public String getLogradouro_curto()
	{
		return logradouro_curto;
	}

	public void setLogradouro_curto(String logradouro_curto)
	{
		this.logradouro_curto = logradouro_curto;
	}

}
